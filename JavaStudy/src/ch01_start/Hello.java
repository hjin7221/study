package ch01_start;

public class Hello {

	public static void main(String[] args) {
		
		// 실행 단축키 [Ctrl + F11]
		System.out.println("Hello World");
		
		// 코드라인 복사(Copy Lines)
		// 단축키 [Ctrl + Alt + Down(방향키 아래)]
		// 자동완성 단축키 [Ctrl + Space]
		// syso -> System.out.println()
		System.out.println("오늘은 화요일");
		System.out.println("내일은 수요일");
		// 코드라인 삭제(Delete Line)
		// 단축키 [Ctrl + D]
		System.out.println("모레은 목요일");
		
		// 테스트용 주석
		// 테스트용 주석 추가
		
		// 실행 취소의 원복 (Redo)
		// 단축키 [Ctrl + Y]
		System.out.println();
	}

}
